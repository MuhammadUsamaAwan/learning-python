# string
greeting = "Hello, World!"
print(greeting)
print(type(greeting))
first_name = "John"
last_name = "Doe"
print(first_name + " " + last_name)

print("")

# integer
age = 21
age += 1
print(age)
print(type(age))
print("Your age is " + str(age) + ".")

print("")

# float
height = 250.5
print(height)
print(type(height))
print("Your height is " + str(height) + " cm.")

print("")

# boolean
human = True
print(human)
print(type(human))
print("Are you a human? " + str(human))
